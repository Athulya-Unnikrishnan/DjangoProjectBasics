def add(num1,num2):
    sum=num1+num2
    return sum

def difference(num1,num2):
    diff=num2-num1
    return diff

def product(num1,num2):
    product=num1*num2
    return product

res=add(100,200)
res1=difference(300,500)
res2=product(2,6)
print("sum is ",res)
print("Difference is ",res1)
print("Product is ",res2)
